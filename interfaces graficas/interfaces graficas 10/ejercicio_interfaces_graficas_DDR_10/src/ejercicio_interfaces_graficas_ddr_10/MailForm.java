    private void btnEnviarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEnviarActionPerformed

        String asunto = this.txtAsunto.getText();
        String correo = this.txtCorreo.getText();
        String mensaje = this.txtMensaje.getText();

        String errores = "";

        if (correo.equals("")) {
            errores += "- El correo no puedo estar vacio \n";
        } else if (!MetodosSueltos.validar_Mail_Exp(correo)) {
            errores += "- no es valido el correo \n";
        }

        if (mensaje.equals("")) {
            errores += "- El mensaje no puedo estar vacio \n";
        }

        if (errores.equals("")) {

            try {
                mail.enviarEmail(asunto, mensaje, correo);
                JOptionPane.showMessageDialog(this, "Se ha enviado correctamente", "Ã‰xito", JOptionPane.INFORMATION_MESSAGE);
            } catch (MessagingException ex) {
                JOptionPane.showMessageDialog(this, "Error: " + ex.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
            }

        } else {
            JOptionPane.showMessageDialog(this, errores, "Error", JOptionPane.ERROR_MESSAGE);
        }


    }//GEN-LAST:event_btnEnviarActionPerformed

