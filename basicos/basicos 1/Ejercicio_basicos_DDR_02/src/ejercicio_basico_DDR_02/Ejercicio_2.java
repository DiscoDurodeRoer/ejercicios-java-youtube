package ejercicio_basico_DDR_02;

public class Ejercicio_2 {

    public static void main(String[] args) {
        
        //Declaramos dos variables numericas
        int a=5;
        int b=2;
        
        //Declaramos variables que almacenaran las operaciones
        int suma=a+b;
        int resta=a-b;
        int multiplicacion=a*b;
        double division=(double)a/b;
        int resto=a%b;
        
        //Mostramos las anteriores variables
        System.out.println("La suma de a y b es "+suma);
        System.out.println("La resta de a y b es "+resta);
        System.out.println("La multiplicación de a y b es "+multiplicacion);
        System.out.println("La división de a y b es "+division);
        System.out.println("El resto de a y b es "+resto);
        
    }
    
}
